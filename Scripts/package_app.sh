CODESIGN_ARGS_NO_TIMESTAMP=(--force --options runtime --sign "${CODESIGN_ID}")
function resign() {
  xattr -cr "$1" 2>/dev/null || true
  if ! codesign "${CODESIGN_ARGS[@]}" "$1" 2>&1; then
    if [[ " ${CODESIGN_ARGS[*]} " == *" --timestamp "* ]]; then
      echo "  timestamp failed, retrying without timestamp: $1" >&2
      codesign "${CODESIGN_ARGS_NO_TIMESTAMP[@]}" "$1"
    else
      return 1
    fi
  fi
}

copy_app_bundle() {
  local source="$1"
  local destination="$2"
  rm -rf "$destination"
  mkdir -p "$(dirname "$destination")"
  ditto --noextattr --noqtn "$source" "$destination"
  xattr -cr "$destination" 2>/dev/null || true
}

seal_app_bundle_copy() {
  local bundle="$1"
  local sparkle="${bundle}/Contents/Frameworks/Sparkle.framework"
  local widget="${bundle}/Contents/PlugIns/${WIDGET_PRODUCT_NAME}.appex"

  if [[ "$SIGNING_MODE" == "adhoc" ]]; then
    xattr -cr "$bundle" 2>/dev/null || true
    if [[ -d "$sparkle" ]]; then
      resign "$sparkle"
      resign "$sparkle/Versions/B/Sparkle"
      resign "$sparkle/Versions/B/Autoupdate"
      resign "$sparkle/Versions/B/Updater.app"
      resign "$sparkle/Versions/B/Updater.app/Contents/MacOS/Updater"
      resign "$sparkle/Versions/B/XPCServices/Downloader.xpc"
      resign "$sparkle/Versions/B/XPCServices/Downloader.xpc/Contents/MacOS/Downloader"
      resign "$sparkle/Versions/B/XPCServices/Installer.xpc"
      resign "$sparkle/Versions/B/XPCServices/Installer.xpc/Contents/MacOS/Installer"
      resign "$sparkle/Versions/B"
      resign "$sparkle"
    fi

    if [[ -f "${bundle}/Contents/Helpers/${CLI_EXECUTABLE_NAME}" ]]; then
      resign "${bundle}/Contents/Helpers/${CLI_EXECUTABLE_NAME}"
    fi
    if [[ -f "${bundle}/Contents/Helpers/${WATCHDOG_EXECUTABLE_NAME}" ]]; then
      resign "${bundle}/Contents/Helpers/${WATCHDOG_EXECUTABLE_NAME}"
    fi

    if [[ -d "$widget" ]]; then
      resign "${widget}/Contents/MacOS/${WIDGET_PRODUCT_NAME}"
      codesign --force --sign - \
        --entitlements "$WIDGET_ENTITLEMENTS" \
        "$widget"
    fi

    codesign --force --sign - \
      --entitlements "$APP_ENTITLEMENTS" \
      "$bundle"
    return
  fi

  if [[ -d "$sparkle" ]]; then
    resign "$sparkle"
    resign "$sparkle/Versions/B/Sparkle"
    resign "$sparkle/Versions/B/Autoupdate"
    resign "$sparkle/Versions/B/Updater.app"
    resign "$sparkle/Versions/B/Updater.app/Contents/MacOS/Updater"
    resign "$sparkle/Versions/B/XPCServices/Downloader.xpc"
    resign "$sparkle/Versions/B/XPCServices/Downloader.xpc/Contents/MacOS/Downloader"
    resign "$sparkle/Versions/B/XPCServices/Installer.xpc"
    resign "$sparkle/Versions/B/XPCServices/Installer.xpc/Contents/MacOS/Installer"
    resign "$sparkle/Versions/B"
    resign "$sparkle"
  fi

  if [[ -f "${bundle}/Contents/Helpers/${CLI_EXECUTABLE_NAME}" ]]; then
    resign "${bundle}/Contents/Helpers/${CLI_EXECUTABLE_NAME}"
  fi
  if [[ -f "${bundle}/Contents/Helpers/${WATCHDOG_EXECUTABLE_NAME}" ]]; then
    resign "${bundle}/Contents/Helpers/${WATCHDOG_EXECUTABLE_NAME}"
  fi

  if [[ -d "$widget" ]]; then
    resign "${widget}/Contents/MacOS/${WIDGET_PRODUCT_NAME}"
    codesign "${CODESIGN_ARGS[@]}" \
      --entitlements "$WIDGET_ENTITLEMENTS" \
      "$widget"
  fi

  codesign "${CODESIGN_ARGS[@]}" \
    --entitlements "$APP_ENTITLEMENTS" \
    "$bundle"
}
  # Sign innermost binaries first, then the framework root to seal resources
  resign "$SPARKLE"
  resign "$SPARKLE/Versions/B/Sparkle"
  resign "$SPARKLE/Versions/B/Autoupdate"
  resign "$SPARKLE/Versions/B/Updater.app"
  resign "$SPARKLE/Versions/B/Updater.app/Contents/MacOS/Updater"
  resign "$SPARKLE/Versions/B/XPCServices/Downloader.xpc"
  resign "$SPARKLE/Versions/B/XPCServices/Downloader.xpc/Contents/MacOS/Downloader"
  resign "$SPARKLE/Versions/B/XPCServices/Installer.xpc"
  resign "$SPARKLE/Versions/B/XPCServices/Installer.xpc/Contents/MacOS/Installer"
  resign "$SPARKLE/Versions/B"
  resign "$SPARKLE"
fi

if [[ -f "$ICON_TARGET" ]]; then
  cp "$ICON_TARGET" "$APP/Contents/Resources/Icon.icns"
fi

# Bundle app resources (provider icons, etc.).
APP_RESOURCES_DIR="$ROOT/Sources/CodexBar/Resources"
if [[ -d "$APP_RESOURCES_DIR" ]]; then
  cp -R "$APP_RESOURCES_DIR/." "$APP/Contents/Resources/"
fi
if [[ ! -f "$APP/Contents/Resources/Icon-classic.icns" ]]; then
  echo "ERROR: Missing Icon-classic.icns in app bundle resources." >&2
  exit 1
fi

# SwiftPM resource bundles (e.g. KeyboardShortcuts) are emitted next to the built binary.
CODEXBAR_BINARY="$(resolve_binary_path "$APP_SWIFTPM_PRODUCT" "${ARCH_LIST[0]}")"
PREFERRED_BUILD_DIR="$(dirname "${CODEXBAR_BINARY:-$(build_product_path "$APP_SWIFTPM_PRODUCT" "${ARCH_LIST[0]}")}")"
shopt -s nullglob
SWIFTPM_BUNDLES=("${PREFERRED_BUILD_DIR}/"*.bundle)
shopt -u nullglob
if [[ ${#SWIFTPM_BUNDLES[@]} -gt 0 ]]; then
  for bundle in "${SWIFTPM_BUNDLES[@]}"; do
    bundle_name="$(basename "$bundle")"
    cp -R "$bundle" "$APP/Contents/Resources/"
  done
fi
if [[ ! -d "$APP/Contents/Resources/KeyboardShortcuts_KeyboardShortcuts.bundle" ]]; then
  echo "ERROR: Missing KeyboardShortcuts SwiftPM resource bundle (Settings → Keyboard shortcut will crash)." >&2
  echo "Expected: ${PREFERRED_BUILD_DIR}/KeyboardShortcuts_KeyboardShortcuts.bundle" >&2
  exit 1
fi

# Ensure contents are writable before stripping attributes and signing.
chmod -R u+w "$APP"

# Strip extended attributes to prevent AppleDouble (._*) files that break code sealing
xattr -cr "$APP"
find "$APP" -name '._*' -delete

# Strip extended attributes again right before signing (Dropbox re-adds them continuously)
xattr -cr "$APP" 2>/dev/null || true
find "$APP" -name '._*' -delete 2>/dev/null || true

# Sign helper binaries if present
if [[ -f "${APP}/Contents/Helpers/${CLI_EXECUTABLE_NAME}" ]]; then
  resign "${APP}/Contents/Helpers/${CLI_EXECUTABLE_NAME}"
fi
if [[ -f "${APP}/Contents/Helpers/${WATCHDOG_EXECUTABLE_NAME}" ]]; then
  resign "${APP}/Contents/Helpers/${WATCHDOG_EXECUTABLE_NAME}"
fi

# Sign widget extension if present
if [[ -d "${APP}/Contents/PlugIns/${WIDGET_PRODUCT_NAME}.appex" ]]; then
  resign "${APP}/Contents/PlugIns/${WIDGET_PRODUCT_NAME}.appex/Contents/MacOS/${WIDGET_PRODUCT_NAME}"
  codesign "${CODESIGN_ARGS[@]}" \
    --entitlements "$WIDGET_ENTITLEMENTS" \
    "$APP/Contents/PlugIns/${WIDGET_PRODUCT_NAME}.appex"
fi

# Embed provisioning profile.
# REQUIRED for release + real-signed builds: app entitlements include
# com.apple.application-identifier, which AMFI checks at launch against
# the embedded profile. A bundle without this file passes codesign /
# spctl / notarization / stapler but fails to launch with "Launchd job
# spawn failed" (POSIX 163). Fail loud here instead of shipping a
# broken bundle that only breaks on the user's Mac.
PROVISION_PROFILE="$ROOT/Provisioning/QuotaKit_Dev.provisionprofile"
if [[ ! -f "$PROVISION_PROFILE" && -f "$ROOT/Provisioning/CodexBar_Dev.provisionprofile" ]]; then
  PROVISION_PROFILE="$ROOT/Provisioning/CodexBar_Dev.provisionprofile"
fi
if [[ "${CONF}" == "release" && "${SIGNING_MODE:-}" != "adhoc" ]]; then
  if [[ ! -f "$PROVISION_PROFILE" ]]; then
    echo "FATAL: provisioning profile not found at $PROVISION_PROFILE" >&2
    echo "" >&2
    echo "  This file is gitignored and local-only. In a worktree or fresh" >&2
    echo "  checkout, copy it from the main repo:" >&2
    echo "    cp -R <main-repo>/Provisioning $ROOT/" >&2
    echo "" >&2
    echo "  Without it the bundle ships missing Contents/embedded.provisionprofile" >&2
    echo "  and AMFI rejects the binary at launch (POSIX 163), even though" >&2
    echo "  codesign / spctl / notarization / stapler all pass." >&2
    exit 1
  fi
  cp "$PROVISION_PROFILE" "$APP/Contents/embedded.provisionprofile"
elif [[ -f "$PROVISION_PROFILE" ]]; then
  # Adhoc / debug builds: embed best-effort if available, do not fail.
  cp "$PROVISION_PROFILE" "$APP/Contents/embedded.provisionprofile"
fi

# Strip xattr one final time before signing the app bundle
xattr -cr "$APP" 2>/dev/null || true
find "$APP" -name '._*' -delete 2>/dev/null || true

# Finally sign the app bundle itself
codesign "${CODESIGN_ARGS[@]}" \
  --entitlements "$APP_ENTITLEMENTS" \
  "$APP"

if [[ -n "$STAGED_APP_PATH" ]]; then
  copy_app_bundle "$APP" "$STAGED_APP_PATH"
  seal_app_bundle_copy "$STAGED_APP_PATH"
  echo "Staged $STAGED_APP_PATH"
fi

if [[ -n "$INSTALL_APP_PATH" ]]; then
  copy_app_bundle "$APP" "$INSTALL_APP_PATH"
  seal_app_bundle_copy "$INSTALL_APP_PATH"
  echo "Installed $INSTALL_APP_PATH"
fi

# Move the signed app bundle from /tmp back to the project directory
copy_app_bundle "$APP" "$APP_FINAL"
rm -rf "$(dirname "$APP")"
APP="$APP_FINAL"
echo "Created $APP"
